package JavaPracticeCode;

public class WhileLoop {

	public static void main(String[] args) {

		int i =1;
		while(i<=10) {
			i=i+1;//print even numbers from 1 to 10
			System.out.println(i);
		//	i++; //print 1 to 10
		//	i=i+1; //print odd numbers between 1 to 10
			i++;
			
			
		}
		int j=10;
		while(j>=1) {
			
			System.out.println(j);
		//	j--;//reverse order from 10 to 1
			j=j-1; //reverse even numbers 10 to 1
			j--;
		}
		
	}
}


